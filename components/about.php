            The main idea behind the creation of the <strong>« International Olympiads in Problem Solving (IOPS)
                » </strong>
            is to propose a common contest for both school students and university students.<br>
            The aim of the Olympiads is to showcase the skills in problem solving and competitive programming
            in the computing sciences and engineering among the participating countries.
            <br>
            <br>
            <strong>Thus, the participating contestants can share their proficiencies in information and communication
                technologies.</strong>

            <br><br>
            <br><br>

            <div class="clear"></div>

            The <strong>« International Olympiads in Problem Solving (IOPS) »</strong> is to be organized yearly by one of the
            participating countries.
            For each participating country, a national steering committee is appointed to make intense training and fair
            selection
            of the national delegation.<br>
            The national steering committee is free to choose the adequate procedures for training and conducting local
            and national contests.
            The national delegation is composed of 4 to 12 contestants and two representatives of the national steering
            committee.
            <br>Students compete individually and try to solve the maximum problems by finding the most efficient
            solutions.
            Any school student or university student aged no more than 25 years is eligible to compete in the Olympiad.

